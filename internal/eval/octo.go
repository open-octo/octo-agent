package eval

import (
	"bytes"
	"context"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"strconv"
)

// octoOptions configures how the harness drives the octo binary.
type octoOptions struct {
	Bin       string // path to the octo binary
	EvalHome  string // isolated HOME so octo's ~/.octo never touches the user's
	Model     string // empty = octo default
	Provider  string // empty = octo default
	MaxTurns  int    // octo --max-turns (0 = octo default)
	MaxTokens int    // octo --max-tokens per response (0 = provider default)
	AllowNet  bool   // false adds --sandbox (hermetic: no gold-patch leak via web tools)
}

// driveOcto runs octo as a headless one-shot inside workDir, handing it prompt
// as a single agentic turn. Strict permission mode plus a permissive
// permissions.yml in the isolated HOME lets tools run without prompts;
// --no-save keeps the throwaway session out of history. The
// combined stdout/stderr is returned for the transcript regardless of exit
// code — octo may exit non-zero yet still have made useful edits, which the
// caller captures from the filesystem.
func driveOcto(ctx context.Context, opt octoOptions, workDir, prompt string) (string, error) {
	if err := writeEvalPermissions(opt.EvalHome); err != nil {
		return "", err
	}
	// The prompt file lives outside workDir so verify never sees it and octo's
	// own edits don't accidentally include it.
	promptPath := filepath.Join(opt.EvalHome, "prompt.txt")
	if err := os.WriteFile(promptPath, []byte(prompt), 0o644); err != nil {
		return "", fmt.Errorf("write prompt file: %w", err)
	}

	args := []string{"--tools", "--permission-mode", "strict", "--no-save", "--plain", "--prompt-file", promptPath}
	if !opt.AllowNet {
		args = append(args, "--sandbox")
	}
	if opt.MaxTurns > 0 {
		args = append(args, "--max-turns", strconv.Itoa(opt.MaxTurns))
	}
	if opt.MaxTokens > 0 {
		// A large single-file artifact (a full HTML homepage) is emitted as one
		// write_file tool call, so its content counts against the per-response
		// output cap. The provider default (e.g. 4096 on DeepSeek) truncates it
		// mid-write and nothing lands; a higher cap gives generative tasks room.
		args = append(args, "--max-tokens", strconv.Itoa(opt.MaxTokens))
	}
	if opt.Model != "" {
		args = append(args, "--model", opt.Model)
	}
	if opt.Provider != "" {
		args = append(args, "--provider", opt.Provider)
	}

	cmd := exec.CommandContext(ctx, opt.Bin, args...)
	cmd.Dir = workDir
	cmd.Env = append(os.Environ(), "HOME="+opt.EvalHome)
	var buf bytes.Buffer
	cmd.Stdout = &buf
	cmd.Stderr = &buf
	err := cmd.Run()
	return buf.String(), err
}

// writeEvalPermissions drops a permissive permissions.yml into the eval HOME so
// octo runs its tools without interactive prompts in the throwaway copy.
func writeEvalPermissions(evalHome string) error {
	dir := filepath.Join(evalHome, ".octo")
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	const perms = `# Generated by octo-eval — allow tools non-interactively in the eval sandbox.
terminal:
  - allow: { pattern: "" }
write_file:
  - allow: { path: ["**"] }
edit_file:
  - allow: { path: ["**"] }
read_file:
  - allow: { path: ["**"] }
`
	return os.WriteFile(filepath.Join(dir, "permissions.yml"), []byte(perms), 0o644)
}
